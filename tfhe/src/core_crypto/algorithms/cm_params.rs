#[derive(Copy, Clone)]
pub struct CmApParams {
    pub cm_dimension: CmDimension,
    pub precision: usize,
    pub log_nu: f64,
    pub level_ks: DecompositionLevelCount,
    pub level_bs: DecompositionLevelCount,
    pub glwe_dimension: GlweDimension,
    pub polynomial_size: PolynomialSize,
    pub lwe_dimension: LweDimension,
    pub base_log_ks: DecompositionBaseLog,
    pub base_log_bs: DecompositionBaseLog,
    pub exp_nb_zeros_n: f64,
    pub max_nb_zeros_n: f64,
    pub ms_bound_n: NoiseEstimationMeasureBound,
    pub r_sigma_factor_n: RSigmaFactor,
    pub ms_input_variance_n: Variance,
    pub peak_noise_n: f64,
    pub lwe_std_dev: f64,
    pub glwe_std_dev: f64,
    pub cost: f64,
    pub p_fail: f64,
}

impl CmApParams {
    const fn new(
        (
            cm_dimension,
            precision,
            log_nu,
            level_ks,
            level_bs,
            glwe_dimension,
            polynomial_size,
            lwe_dimension,
            base_ks,
            base_bs,
            exp_nb_zeros_n,
            max_nb_zeros_n,
            ms_bound_n,
            r_sigma_factor_n,
            ms_input_variance_n,
            peak_noise_n,
            lwe_std_dev,
            glwe_std_dev,
            glwe_std_dev2,
            cost,
            p_fail,
        ): RawType,
    ) -> Self {
        assert!(base_ks.is_power_of_two());
        assert!(base_bs.is_power_of_two());

        assert!(glwe_std_dev == glwe_std_dev2);

        let base_log_ks = base_ks.ilog2() as usize;
        let base_log_bs = base_bs.ilog2() as usize;

        Self {
            cm_dimension: CmDimension(cm_dimension),
            precision,
            log_nu,
            level_ks: DecompositionLevelCount(level_ks),
            level_bs: DecompositionLevelCount(level_bs),
            glwe_dimension: GlweDimension(glwe_dimension),
            polynomial_size: PolynomialSize(polynomial_size),
            lwe_dimension: LweDimension(lwe_dimension),
            base_log_ks: DecompositionBaseLog(base_log_ks),
            base_log_bs: DecompositionBaseLog(base_log_bs),
            exp_nb_zeros_n,
            max_nb_zeros_n,
            ms_bound_n: NoiseEstimationMeasureBound(ms_bound_n as f64),
            r_sigma_factor_n: RSigmaFactor(r_sigma_factor_n),
            ms_input_variance_n: Variance(ms_input_variance_n),
            peak_noise_n,
            lwe_std_dev,
            glwe_std_dev,
            cost,
            p_fail,
        }
    }
}

