/// Asserts every lane of a batched run reproduces `expected`, bit for bit.
fn assert_all_lanes_match(batch: &[Vec<u64>], expected: &[u64], label: &str) {
    let batch_size = batch.len();
    assert!(batch_size > 0, "{label}: empty batch");
    for (lane, ct) in batch.iter().enumerate() {
        assert_eq!(
            ct.as_slice(),
            expected,
            "{label}: batched GPU PBS lane {lane}/{batch_size} does not match the golden data"
        );
    }
}

#[test]
fn test_regression_classical_pbs_golden() {
    if !hopper_or_newer_is_supported() {
        println!(
            "skipping test_regression_classical_pbs_golden: the PBS golden data \
             requires compute capability 9.x (H100) or newer"
        );
        return;
    }

    // Random batch size: the golden output is per-bootstrap and must not depend
    // on how many bootstraps share the batched call. The size is printed so a
    // failure can be reproduced deterministically.
    let batch_size = rand::thread_rng().gen_range(1..=MAX_REGRESSION_BATCH_SIZE);
    println!("test_regression_classical_pbs_golden: batch size {batch_size}");

    let batch =
        run_classical_pbs_golden_batch(PARAM_MESSAGE_2_CARRY_2_KS_PBS_TUNIFORM_2M128, batch_size);
    assert_all_lanes_match(
        &batch,
        CLASSICAL_EXPECTED,
        "PARAM_MESSAGE_2_CARRY_2_KS_PBS_TUNIFORM_2M128",
    );
}

