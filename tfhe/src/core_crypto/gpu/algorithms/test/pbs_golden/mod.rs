// ============================================================================
// Parallel-execution robustness stress test.
//
// Where the tests above validate a single batched call, this drives
// `NUM_PARALLEL_STREAMS` worker threads *concurrently*, each on its own CUDA
// stream, each launching a batch of a random size in
// `[1, MAX_PARALLEL_BATCH_SIZE]`. Every bootstrap in every batch, on every
// thread, is initialized with the same golden input, so every output lane —
// across all threads — must still reproduce the committed golden ciphertext bit
// for bit. This catches races, per-stream state leakage and scheduling-dependent
// non-determinism that a single-stream run cannot.
//
// The whole setup (keys, encrypted golden input, per-thread device buffers and
// the random batch sizes) is built in a preprocessing phase and fully
// materialized (`cuda_synchronize_device`) *before* any thread launches, so the
// parallel section contains only the concurrent bootstrap launches.
// ============================================================================

/// Per-thread device-side work prepared during preprocessing: an owned stream
/// plus the batched input / index / output buffers sized for that thread's
/// randomly chosen batch size. The bootstrap key and accumulator are shared
/// read-only across all threads, so they are deliberately *not* stored here.
struct ThreadWork {
    stream: CudaStreams,
    batch_size: usize,
    d_input: CudaLweCiphertextList<u64>,
    d_output: CudaLweCiphertextList<u64>,
    d_test_vector_indexes: CudaVec<u64>,
    d_input_indexes: CudaVec<u64>,
    d_output_indexes: CudaVec<u64>,
}

/// Preprocessing phase: allocates one stream per worker thread and builds, on
/// that stream, the replicated golden input, output and index buffers for a
/// random batch size in `[1, MAX_PARALLEL_BATCH_SIZE]`. All device work is
/// synchronized before returning, so the caller can launch every thread's
/// bootstrap in parallel with no outstanding transfers.
fn preprocess_thread_work(
    lwe_ciphertext_in: &LweCiphertextOwned<u64>,
    output_lwe_dimension: LweDimension,
    ciphertext_modulus: CiphertextModulus<u64>,
) -> Vec<ThreadWork> {
    let mut rng = rand::thread_rng();
    let work: Vec<ThreadWork> = (0..NUM_PARALLEL_STREAMS)
        .map(|_| {
            let batch_size = rng.gen_range(1..=MAX_PARALLEL_BATCH_SIZE);
            let stream = CudaStreams::new_single_gpu(GpuIndex::new(0));

            let d_input =
                replicated_input(lwe_ciphertext_in, ciphertext_modulus, &stream, batch_size);
            let d_output = CudaLweCiphertextList::new(
                output_lwe_dimension,
                LweCiphertextCount(batch_size),
                ciphertext_modulus,
                &stream,
            );
            let (d_test_vector_indexes, d_input_indexes, d_output_indexes) =
                batch_indexes(&stream, batch_size);

            ThreadWork {
                stream,
                batch_size,
                d_input,
                d_output,
                d_test_vector_indexes,
                d_input_indexes,
                d_output_indexes,
            }
        })
        .collect();

    // Materialize every preprocessing transfer before the parallel phase so no
    // thread races an in-flight upload on another stream.
    unsafe { cuda_synchronize_device(0) };

    println!(
        "parallel PBS stress: {NUM_PARALLEL_STREAMS} streams, batch sizes {:?}",
        work.iter().map(|w| w.batch_size).collect::<Vec<_>>()
    );

    work
}

/// Launches every thread's bootstrap concurrently (one OS thread per stream via
/// `std::thread::scope`), waits for all of them, and returns each thread's raw
/// per-lane output ciphertext limbs. `launch` performs the actual (variant-
/// specific) bootstrap call for one [`ThreadWork`]; it is shared by reference
/// across threads, so it must only *read* the bootstrap key / accumulator it
/// captures.
fn spawn_and_collect<F>(work: &mut [ThreadWork], launch: F) -> Vec<Vec<Vec<u64>>>
where
    F: Fn(&mut ThreadWork) + Sync,
{
    std::thread::scope(|scope| {
        let handles: Vec<_> = work
            .iter_mut()
            .map(|w| {
                let launch = &launch;
                scope.spawn(move || {
                    launch(w);
                    let out_host = w.d_output.to_lwe_ciphertext_list(&w.stream);
                    out_host
                        .iter()
                        .map(|ct| ct.as_ref().to_vec())
                        .collect::<Vec<Vec<u64>>>()
                })
            })
            .collect();

        handles
            .into_iter()
            .map(|h| h.join().expect("parallel PBS worker thread panicked"))
            .collect()
    })
}

/// Asserts every lane produced by every thread reproduces `expected`, bit for
/// bit, and that each thread produced exactly its requested batch size.
fn assert_all_threads_match(
    results: &[Vec<Vec<u64>>],
    batch_sizes: &[usize],
    expected: &[u64],
    label: &str,
) {
    for (thread, (lanes, &batch_size)) in results.iter().zip(batch_sizes).enumerate() {
        assert_eq!(
            lanes.len(),
            batch_size,
            "{label}: thread {thread} produced {} lanes, expected {batch_size}",
            lanes.len()
        );
        for (lane, ct) in lanes.iter().enumerate() {
            assert_eq!(
                ct.as_slice(),
                expected,
                "{label}: thread {thread} lane {lane}/{batch_size} does not match the golden data"
            );
        }
    }
}

/// Classical GPU PBS parallel stress run: shared keys + accumulator, then
/// `NUM_PARALLEL_STREAMS` concurrent batched bootstraps of the golden input.
/// Returns each thread's per-lane output limbs and the batch size it ran.
fn run_classical_pbs_parallel_stress(
    params: ClassicPBSParameters,
) -> (Vec<Vec<Vec<u64>>>, Vec<usize>) {
    let input_lwe_dimension = params.lwe_dimension;
    let lwe_noise_distribution = params.lwe_noise_distribution;
    let glwe_noise_distribution = params.glwe_noise_distribution;
    let ciphertext_modulus = params.ciphertext_modulus;
    let glwe_dimension = params.glwe_dimension;
    let polynomial_size = params.polynomial_size;
    let decomp_base_log = params.pbs_base_log;
    let decomp_level_count = params.pbs_level;
    let msg_modulus = params.message_modulus.0 * params.carry_modulus.0;
    let encoding_with_padding = get_encoding_with_padding(ciphertext_modulus);

    let stream = CudaStreams::new_single_gpu(GpuIndex::new(0));
    let (mut secret_random_generator, mut encryption_random_generator) = deterministic_generators();

    let f = |x: u64| x.wrapping_mul(2).wrapping_sub(1).wrapping_rem(msg_modulus);
    let delta = encoding_with_padding / msg_modulus;

    let accumulator = generate_programmable_bootstrap_glwe_lut(
        polynomial_size,
        glwe_dimension.to_glwe_size(),
        msg_modulus as usize,
        ciphertext_modulus,
        delta,
        f,
    );

    let input_lwe_secret_key = allocate_and_generate_new_binary_lwe_secret_key(
        input_lwe_dimension,
        &mut secret_random_generator,
    );
    let output_glwe_secret_key = allocate_and_generate_new_binary_glwe_secret_key(
        glwe_dimension,
        polynomial_size,
        &mut secret_random_generator,
    );
    let output_lwe_dimension = output_glwe_secret_key
        .clone()
        .into_lwe_secret_key()
        .lwe_dimension();

    let mut bsk = LweBootstrapKey::new(
        0u64,
        glwe_dimension.to_glwe_size(),
        polynomial_size,
        decomp_base_log,
        decomp_level_count,
        input_lwe_dimension,
        ciphertext_modulus,
    );
    par_generate_lwe_bootstrap_key(
        &input_lwe_secret_key,
        &output_glwe_secret_key,
        &mut bsk,
        glwe_noise_distribution,
        &mut encryption_random_generator,
    );
    let d_bsk = CudaLweBootstrapKey::from_lwe_bootstrap_key(&bsk, None, &stream);

    let plaintext = Plaintext(GOLDEN_MESSAGE * delta);
    let lwe_ciphertext_in = allocate_and_encrypt_new_lwe_ciphertext(
        &input_lwe_secret_key,
        plaintext,
        lwe_noise_distribution,
        ciphertext_modulus,
        &mut encryption_random_generator,
    );

    // Accumulator is shared read-only by every thread; build it once here and
    // synchronize together with the bootstrap key before the parallel phase.
    let d_accumulator = CudaGlweCiphertextList::from_glwe_ciphertext(&accumulator, &stream);
    unsafe { cuda_synchronize_device(0) };

    let mut work =
        preprocess_thread_work(&lwe_ciphertext_in, output_lwe_dimension, ciphertext_modulus);
    let batch_sizes: Vec<usize> = work.iter().map(|w| w.batch_size).collect();

    let d_bsk = &d_bsk;
    let d_accumulator = &d_accumulator;
    let results = spawn_and_collect(&mut work, move |w| {
        cuda_programmable_bootstrap_lwe_ciphertext(
            &w.d_input,
            &mut w.d_output,
            d_accumulator,
            &w.d_test_vector_indexes,
            &w.d_output_indexes,
            &w.d_input_indexes,
            d_bsk,
            &w.stream,
        );
    });

    (results, batch_sizes)
}

