// ============================================================================
// Bit-wise regression ("golden values") test for the 64-bit GPU programmable
// bootstrap, for both the classical and the multi-bit PBS, under a *saturating*
// batch.
//
// Mirrors the FFT16x4x16 golden test in `super::fft`, but adapted to stress the
// throughput (batched) PBS path: a single deterministically encrypted input is
// replicated across a batch and bootstrapped in one batched call, so the GPU
// runs many bootstraps concurrently. Every lane of the batch must reproduce, bit
// for bit, the committed golden output ciphertext — which catches both silent
// numerical drift and any per-lane inconsistency in the batched kernel.
//
// This module exercises the golden data three ways:
//   * `test_regression_{classical,multi_bit_group_4}_pbs_golden` — a single batched call at a
//     *random* batch size, asserting the per-bootstrap output is independent of how many bootstraps
//     share the call;
//   * `test_parallel_streams_*` — many concurrent streams, each a randomly sized batch, asserting
//     robustness under parallel execution.
//
// The whole pipeline (key generation, encryption and the bootstrap) is made
// fully deterministic by seeding the CSPRNGs from a fixed `Seed`; the random
// batch sizes are printed so any failure can be reproduced.
//
// The parameters are the production GPU parameters:
//   * classical  : PARAM_MESSAGE_2_CARRY_2_KS_PBS_TUNIFORM_2M128
//   * multi-bit  : PARAM_GPU_MULTI_BIT_GROUP_4_MESSAGE_2_CARRY_2_KS_PBS_TUNIFORM_2M128
//
// The golden data lives in `pbs_golden_data::pbs_golden_v1` (one reference
// ciphertext per parameter set) and is (re)generated by the `generate_pbs_golden`
// maintenance helper below — a plain (non `#[test]`) function; run it on an H100
// (sm_90) to refresh the file, then commit the result.
//
// The GPU FFT is only bit-reproducible within a given GPU generation, so this
// only runs on compute capability 9.x (H100) or newer, exactly like the
// FFT16x4x16 golden test.
//
// NOTE ON THE GOLDEN VALUES: the standard 64-bit GPU PBS uses an f64 (single
// `double`) FFT, whose ~52-bit mantissa leaves only roughly the top 32 bits of
// each output torus coefficient reliable — the low ~32 bits are deterministically
// rounded to zero. This is expected (it is precisely why the backend keeps a
// separate f128 path for high-precision work), so most committed limbs end in
// `00000000`. The regression signal lives in the high bits, and decryption is
// still exact because the message delta (2^60 here) sits far above the rounding.
// ============================================================================

/// Fixed seed driving every deterministic CSPRNG in this module. Changing it
/// invalidates the committed golden data (regenerate with `generate_pbs_golden`).
const GOLDEN_SEED: u128 = 0x0d1c_e5ed_9017_2048;

/// Batch size at which the committed golden data was captured by
/// `generate_pbs_golden`. The regression tests deliberately run at *other*
/// (random) sizes to prove the per-bootstrap output is batch-size independent,
/// so this value only needs to stay fixed for reproducible regeneration.
const BATCH_SIZE: usize = 264;

/// The single message that is encrypted, replicated across the batch and
/// bootstrapped. Any value in `[0, message_modulus)` works.
const GOLDEN_MESSAGE: u64 = 1;

/// Upper bound (inclusive) on the random batch size used by the single-stream
/// random-batch regression tests. Deliberately larger than [`BATCH_SIZE`] to
/// exercise the batched kernels well past the size the golden data was captured
/// at; every lane must still reproduce the golden ciphertext.
const MAX_REGRESSION_BATCH_SIZE: usize = 1024;

/// Number of concurrent worker threads (each with its own CUDA stream) driven by
/// the parallel-execution stress test.
const NUM_PARALLEL_STREAMS: usize = 16;

/// Upper bound (inclusive) on the per-thread random batch size in the parallel
/// stress test. Kept small (well below [`BATCH_SIZE`]) so no single thread
/// saturates the GPU on its own, letting more of the streams actually overlap
/// and stressing concurrent execution harder. The lower bound is 1: a zero-size
/// batch launches no kernel (an idle thread that would exercise nothing), so it
/// is excluded.
const MAX_PARALLEL_BATCH_SIZE: usize = 66;

/// The golden data was captured on an H100 and the GPU FFT is only guaranteed
/// bit-reproducible within a GPU generation, so these tests only run on Hopper
/// (compute capability 9.x) and newer.
///
/// NOTE: this reuses the FFT16x4x16 capability probe, which is implemented in
/// the CUDA backend as a pure `prop.major >= 9` (Hopper / H100+) device query.
fn hopper_or_newer_is_supported() -> bool {
    // Safe: a pure runtime device-capability query with no aliasing concerns.
    unsafe { cuda_fft16x4x16_is_supported_async(0) }
}

/// Builds the deterministic secret- and encryption-random generators used to
/// generate keys and encrypt inputs, seeded from [`GOLDEN_SEED`]. The seed
/// consumption order matches [`TestResources::new`] so the two stay comparable.
fn deterministic_generators() -> (
    SecretRandomGenerator<DefaultRandomGenerator>,
    EncryptionRandomGenerator<DefaultRandomGenerator>,
) {
    let mut deterministic_seeder =
        DeterministicSeeder::<DefaultRandomGenerator>::new(Seed(GOLDEN_SEED));
    let encryption_random_generator = EncryptionRandomGenerator::<DefaultRandomGenerator>::new(
        deterministic_seeder.seed(),
        &mut deterministic_seeder,
    );
    let secret_random_generator =
        SecretRandomGenerator::<DefaultRandomGenerator>::new(deterministic_seeder.seed());
    (secret_random_generator, encryption_random_generator)
}

/// Index vectors for a batched bootstrap of `batch_size` samples: every sample
/// uses the single accumulator (`test_vector` index 0) and maps one-to-one to
/// its own input/output slot.
fn batch_indexes(
    stream: &CudaStreams,
    batch_size: usize,
) -> (CudaVec<u64>, CudaVec<u64>, CudaVec<u64>) {
    let test_vector_indexes = vec![0u64; batch_size];
    let slot_indexes: Vec<u64> = (0..batch_size as u64).collect();

    let mut d_test_vector_indexes = unsafe { CudaVec::<u64>::new_async(batch_size, stream, 0) };
    let mut d_input_indexes = unsafe { CudaVec::<u64>::new_async(batch_size, stream, 0) };
    let mut d_output_indexes = unsafe { CudaVec::<u64>::new_async(batch_size, stream, 0) };
    unsafe {
        d_test_vector_indexes.copy_from_cpu_async(&test_vector_indexes, stream, 0);
        d_input_indexes.copy_from_cpu_async(&slot_indexes, stream, 0);
        d_output_indexes.copy_from_cpu_async(&slot_indexes, stream, 0);
    }
    (d_test_vector_indexes, d_input_indexes, d_output_indexes)
}

/// Replicates one input LWE ciphertext into a device list of `batch_size`
/// identical copies.
fn replicated_input(
    lwe_ciphertext_in: &LweCiphertextOwned<u64>,
    ciphertext_modulus: CiphertextModulus<u64>,
    stream: &CudaStreams,
    batch_size: usize,
) -> CudaLweCiphertextList<u64> {
    let lwe_size = lwe_ciphertext_in.lwe_size();
    let mut container = Vec::with_capacity(lwe_size.0 * batch_size);
    for _ in 0..batch_size {
        container.extend_from_slice(lwe_ciphertext_in.as_ref());
    }
    let h_input = LweCiphertextList::from_container(container, lwe_size, ciphertext_modulus);
    CudaLweCiphertextList::from_lwe_ciphertext_list(&h_input, stream)
}

/// Runs the classical 64-bit GPU PBS on a batch of `batch_size` identical inputs
/// (a single deterministically encrypted [`GOLDEN_MESSAGE`]) and returns the raw
/// output ciphertext limbs (mask followed by body) of every lane.
fn run_classical_pbs_golden_batch(
    params: ClassicPBSParameters,
    batch_size: usize,
) -> Vec<Vec<u64>> {
    let input_lwe_dimension = params.lwe_dimension;
    let lwe_noise_distribution = params.lwe_noise_distribution;
    let glwe_noise_distribution = params.glwe_noise_distribution;
    let ciphertext_modulus = params.ciphertext_modulus;
    let glwe_dimension = params.glwe_dimension;
    let polynomial_size = params.polynomial_size;
    let decomp_base_log = params.pbs_base_log;
    let decomp_level_count = params.pbs_level;
    // Full plaintext space of the parameter set (message * carry).
    let msg_modulus = params.message_modulus.0 * params.carry_modulus.0;
    let encoding_with_padding = get_encoding_with_padding(ciphertext_modulus);

    let stream = CudaStreams::new_single_gpu(GpuIndex::new(0));

    let (mut secret_random_generator, mut encryption_random_generator) = deterministic_generators();

    let f = |x: u64| x.wrapping_mul(2).wrapping_sub(1).wrapping_rem(msg_modulus);
    let delta = encoding_with_padding / msg_modulus;

    let accumulator = generate_programmable_bootstrap_glwe_lut(
        polynomial_size,
        glwe_dimension.to_glwe_size(),
        msg_modulus as usize,
        ciphertext_modulus,
        delta,
        f,
    );

    let input_lwe_secret_key = allocate_and_generate_new_binary_lwe_secret_key(
        input_lwe_dimension,
        &mut secret_random_generator,
    );
    let output_glwe_secret_key = allocate_and_generate_new_binary_glwe_secret_key(
        glwe_dimension,
        polynomial_size,
        &mut secret_random_generator,
    );
    let output_lwe_secret_key = output_glwe_secret_key.clone().into_lwe_secret_key();
    let output_lwe_dimension = output_lwe_secret_key.lwe_dimension();

    let mut bsk = LweBootstrapKey::new(
        0u64,
        glwe_dimension.to_glwe_size(),
        polynomial_size,
        decomp_base_log,
        decomp_level_count,
        input_lwe_dimension,
        ciphertext_modulus,
    );
    par_generate_lwe_bootstrap_key(
        &input_lwe_secret_key,
        &output_glwe_secret_key,
        &mut bsk,
        glwe_noise_distribution,
        &mut encryption_random_generator,
    );
    let d_bsk = CudaLweBootstrapKey::from_lwe_bootstrap_key(&bsk, None, &stream);

    let plaintext = Plaintext(GOLDEN_MESSAGE * delta);
    let lwe_ciphertext_in = allocate_and_encrypt_new_lwe_ciphertext(
        &input_lwe_secret_key,
        plaintext,
        lwe_noise_distribution,
        ciphertext_modulus,
        &mut encryption_random_generator,
    );

    let d_input = replicated_input(&lwe_ciphertext_in, ciphertext_modulus, &stream, batch_size);
    let mut d_output = CudaLweCiphertextList::new(
        output_lwe_dimension,
        LweCiphertextCount(batch_size),
        ciphertext_modulus,
        &stream,
    );
    let d_accumulator = CudaGlweCiphertextList::from_glwe_ciphertext(&accumulator, &stream);
    let (d_test_vector_indexes, d_input_indexes, d_output_indexes) =
        batch_indexes(&stream, batch_size);

    cuda_programmable_bootstrap_lwe_ciphertext(
        &d_input,
        &mut d_output,
        &d_accumulator,
        &d_test_vector_indexes,
        &d_output_indexes,
        &d_input_indexes,
        &d_bsk,
        &stream,
    );

    let out_host = d_output.to_lwe_ciphertext_list(&stream);

    // Sanity check on the first lane: the frozen golden output must also be a
    // *correct* PBS result, so we never enshrine garbage as golden data.
    let lane0 = out_host.iter().next().unwrap();
    let decrypted = decrypt_lwe_ciphertext(&output_lwe_secret_key, &lane0);
    let decoded = round_decode(decrypted.0, delta) % msg_modulus;
    assert_eq!(
        decoded,
        f(GOLDEN_MESSAGE),
        "classical GPU PBS produced an incorrect result for msg={GOLDEN_MESSAGE}"
    );

    out_host.iter().map(|ct| ct.as_ref().to_vec()).collect()
}

