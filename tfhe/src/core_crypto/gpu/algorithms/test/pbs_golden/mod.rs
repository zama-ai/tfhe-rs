/// Formats a `pub const <name>: &[u64] = &[...]` block of hex limbs.
fn format_golden_const(name: &str, values: &[u64]) -> String {
    let mut body = String::new();
    for v in values {
        let _ = writeln!(body, "    {v:#018x},");
    }
    format!("pub const {name}: &[u64] = &[\n{body}];\n\n")
}

/// Asserts every lane of a batched run agrees, and returns lane 0 as the golden
/// reference ciphertext.
fn golden_from_batch(batch: &[Vec<u64>], label: &str) -> Vec<u64> {
    let reference = batch[0].clone();
    assert_all_lanes_match(batch, &reference, label);
    reference
}

/// (Re)generates the PBS golden file. This is *not* a test — it is a manual
/// maintenance helper. To refresh the golden data, temporarily annotate it with
/// `#[test]` `#[ignore]`, run it on an H100 (sm_90) with
/// `cargo test ... -- --ignored --exact`, then commit the regenerated file and
/// remove the annotations again.
#[allow(dead_code)]
fn generate_pbs_golden() {
    if !hopper_or_newer_is_supported() {
        println!(
            "skipping generate_pbs_golden: the PBS golden data requires compute \
             capability 9.x (H100) or newer"
        );
        return;
    }

    let classical = golden_from_batch(
        &run_classical_pbs_golden_batch(PARAM_MESSAGE_2_CARRY_2_KS_PBS_TUNIFORM_2M128, BATCH_SIZE),
        "classical",
    );
    let multi_bit = golden_from_batch(
        &run_multi_bit_pbs_golden_batch(
            PARAM_GPU_MULTI_BIT_GROUP_4_MESSAGE_2_CARRY_2_KS_PBS_TUNIFORM_2M128,
            BATCH_SIZE,
        ),
        "multi_bit_group_4",
    );

    let mut contents = String::from(
        "// @generated by the `generate_pbs_golden` helper in\n\
         // core_crypto::gpu::algorithms::test::pbs_golden. Do not edit by hand.\n",
    );
    contents += &format_golden_const("CLASSICAL_EXPECTED", &classical);
    contents += &format_golden_const("MULTI_BIT_GROUP_4_EXPECTED", &multi_bit);

    // Normalize to a single trailing newline so the generated file is
    // rustfmt-stable and needs no manual formatting pass after regeneration.
    let contents = format!("{}\n", contents.trim_end());

    let path = concat!(
        env!("CARGO_MANIFEST_DIR"),
        "/src/core_crypto/gpu/algorithms/test/pbs_golden/pbs_golden_data/pbs_golden_v1.rs"
    );
    std::fs::write(path, contents).expect("failed to write PBS golden file");
    println!("wrote PBS golden data to {path}");
}
