impl std::ops::Deref for HpuCluster {
    type Target = HashMap<u8, HpuNodeWrapped>;

    fn deref(&self) -> &Self::Target {
        &self.nodes
    }
}

/// Wrapped HpuCluster inside an Arc to easily shared a read-only reference
#[derive(Clone)]
pub struct HpuClusterWrapped(Arc<HpuCluster>);

impl HpuClusterWrapped {
    pub fn new_wrapped(config: &HpuConfig, force_reload: bool) -> Result<Self, HpuInstError> {
        let cluster = HpuCluster::new(config, force_reload)?;
        Ok(Self(Arc::new(cluster)))
    }
}

impl std::ops::Deref for HpuClusterWrapped {
    type Target = Arc<HpuCluster>;

    fn deref(&self) -> &Self::Target {
        &self.0
    }
}

impl HpuClusterWrapped {
    /// Construct an Hpu variable from a vector of HpuLweCiphertext
    /// Allocation is made on the less loaded node
    ///
    /// _NB_: Workload management is done at variable allocation. It's not perfect but easier in
    /// first time.
    pub fn new_var_from(
        &self,
        ct: Vec<HpuLweCiphertextOwned<u64>>,
        mode: crate::asm::iop::VarMode,
        pos: Option<crate::asm::PhysId>,
    ) -> HpuVarWrapped {
        // Compute workload and allocate on the less loaded node
        let trgt_id = if let Some(hid) = pos {
            hid
        } else {
            let hid = self
                .nodes
                .keys()
                .map(|id| {
                    (
                        *id,
                        self.workload[*id as usize].load(atomic::Ordering::SeqCst),
                    )
                })
                .sorted_by(|a, b| Ord::cmp(&a.1, &b.1))
                .map(|x| x.0)
                .next()
                .expect("HpuCluster must contains at least one HpuNode");
            PhysId(hid)
        };

        tracing::debug!("HpuVariable will be created on Hpu {trgt_id}");

        HpuVarWrapped::new_from(trgt_id, self.clone(), self.params.clone(), ct, mode)
    }
}
