// ============================================================================
//  Backup: generate twisting twiddles at runtime
//
//  Fills h_tw[0..n-1] with psi_k = exp(-i*pi*k / 2048) on the host.
//  Use this to verify the hardcoded table above or to populate a custom
//  (non-const) device symbol via cudaMemcpyToSymbol.
//
//  Example:
//    double2 h_tw[512];
//    gen_twisting_twiddles(h_tw, 512);
//    cudaMemcpyToSymbol(my_device_array, h_tw, sizeof(h_tw));
// ============================================================================
static inline void gen_twisting_twiddles(double2 *h_tw, int n) {
  for (int k = 0; k < n; k++) {
    double angle = -(M_PI * k) / 2048.0;
    h_tw[k] = make_double2(cos(angle), sin(angle));
  }
}
