  double2 h_tw[15][64];
  for (int k = 1; k <= 15; k++) {
    for (int tid = 0; tid < 64; tid++) {
      double angle = -2.0 * M_PI * k * tid / 1024.0;
      h_tw[k - 1][tid] = make_double2(cos(angle), sin(angle));
    }
  }
  cudaMemcpyToSymbol(tw_1024, h_tw, sizeof(h_tw));
}

